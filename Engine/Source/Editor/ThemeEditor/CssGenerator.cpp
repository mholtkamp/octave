// ---- Helpers ----

static std::string FormatColor(const ImVec4& c)
{
    int r = (int)std::round(c.x * 255.0f);
    int g = (int)std::round(c.y * 255.0f);
    int b = (int)std::round(c.z * 255.0f);

    if (r < 0) r = 0; if (r > 255) r = 255;
    if (g < 0) g = 0; if (g > 255) g = 255;
    if (b < 0) b = 0; if (b > 255) b = 255;

    if (std::abs(c.w - 1.0f) < 0.001f)
    {
        char buf[16];
        snprintf(buf, sizeof(buf), "#%02X%02X%02X", r, g, b);
        return buf;
    }
    else
    {
        char buf[48];
        snprintf(buf, sizeof(buf), "rgba(%d, %d, %d, %.2f)", r, g, b, c.w);
        return buf;
    }
}

static std::string FormatPx(float v)
{
    char buf[32];
    if (v == std::floor(v))
        snprintf(buf, sizeof(buf), "%dpx", (int)v);
    else
        snprintf(buf, sizeof(buf), "%.1fpx", v);
    return buf;
}

static std::string FormatVec2Px(const ImVec2& v)
{
    return FormatPx(v.x) + " " + FormatPx(v.y);
}

static std::string FormatFloat(float v)
{
    char buf[32];
    snprintf(buf, sizeof(buf), "%.2f", v);
    return buf;
}

static bool Vec4Equal(const ImVec4& a, const ImVec4& b)
{
    return std::abs(a.x - b.x) < 0.0001f &&
           std::abs(a.y - b.y) < 0.0001f &&
           std::abs(a.z - b.z) < 0.0001f &&
           std::abs(a.w - b.w) < 0.0001f;
}

static bool Vec2Equal(const ImVec2& a, const ImVec2& b)
{
    return std::abs(a.x - b.x) < 0.0001f && std::abs(a.y - b.y) < 0.0001f;
}

static bool FloatEqual(float a, float b)
{
    return std::abs(a - b) < 0.0001f;
}

